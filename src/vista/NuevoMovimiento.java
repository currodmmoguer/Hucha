    public void addManejador(ActionListener al) {
        btnExtraer.addActionListener(al);
        btnIngresar.addActionListener(al);
        btnExtraer.setActionCommand("EXTRAER");
        btnIngresar.setActionCommand("INGRESAR");
    }

    public void setConceptos(List<String> conceptos) {
        comboConcepto.removeAllItems();
        for (String concepto : conceptos) {
            comboConcepto.addItem(concepto);
        }
    }

    public String getConcepto() {
        String concepto = null; 
        if (checkNuevoConcepto.isSelected()) {
            concepto = labelConcepto.getText();
                            
        } else {
            if (comboConcepto.getItemCount() > 0)
                concepto = comboConcepto.getSelectedItem().toString();
        }
        return concepto;
    }

    public double getImporte() throws CuentaException {
        System.out.println(importe.getValue().toString());
        if ((double) importe.getValue() == 0) {
            throw new CuentaException("Importe err√≥neo");
        }
        return (double) importe.getValue();
    }

